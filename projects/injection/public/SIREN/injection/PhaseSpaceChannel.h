namespace phase_space_detail {
inline std::size_t & EvaluationEpoch() {
    static thread_local std::size_t epoch = 0;
    return epoch;
}
inline unsigned int & EvaluationDepth() {
    static thread_local unsigned int depth = 0;
    return depth;
}
class ScopedEvaluationCache {
public:
    ScopedEvaluationCache() {
        if (EvaluationDepth()++ == 0) ++EvaluationEpoch();
    }
    ~ScopedEvaluationCache() { --EvaluationDepth(); }
};
inline bool EvaluationCacheActive() { return EvaluationDepth() > 0; }
} // namespace phase_space_detail

struct PhaseSpaceChannelEvaluation {
    double density = 0.0;
    bool directing_active = true;
};

// A single parameterization of a final-state phase space, used
// in multi-channel importance sampling of interaction/decay
// kinematics.
//
// Each channel knows how to:
//   1. Sample final-state kinematics (writing into an InteractionRecord)
//   2. Evaluate its sampling density at ANY valid phase-space point
//
// Property (2) is essential for multi-channel integration: when
// combining N channels with weights alpha_i, the combined density
// is g(x) = sum_i alpha_i * g_i(x), which requires evaluating
// every channel's density at every sampled point.
//
// Each channel reports a Topology (structural shape of the final
// state) and a Measure (what variables the density is differential
// in).  Channels in a MultiChannelPhaseSpace must share the same
// topology and have measures in the same convertibility group.
class PhaseSpaceChannel {
public:
    virtual ~PhaseSpaceChannel() = default;

    // Sample final-state kinematics.
    //
    // Reads the primary momentum, interaction vertex, and target
    // from the record.  Writes secondary momenta, masses, and types
    // into the record.
    //
    // The record's signature (secondary types) must already be set
    // to indicate which final state this channel should produce.
    virtual void Sample(
        std::shared_ptr<siren::utilities::SIREN_random> random,
        std::shared_ptr<siren::detector::DetectorModel const> detector_model,
        siren::dataclasses::InteractionRecord & record
    ) const = 0;

    // Evaluate this channel's sampling density at the phase-space
    // point described by the record.
    //
    // Must be evaluable for ANY valid phase-space point for this
    // final state, not just points generated by this channel.
    virtual double Density(
        std::shared_ptr<siren::detector::DetectorModel const> detector_model,
        siren::dataclasses::InteractionRecord const & record
    ) const = 0;

    virtual std::string Name() const = 0;

    // Structural shape of the final state.
    virtual PhaseSpaceTopology Topology() const = 0;

    // What variables the density is differential in.
    virtual PhaseSpaceMeasure Measure() const = 0;

    // True if this channel contributes a genuine (non-fallback) proposal at the
    // phase-space point in `record`.  A detector-directed channel overrides this
    // to return false when it falls back to its shared isotropic 1/4pi
    // distribution (directing impossible/unnecessary); channels with no fallback
    // notion (physical, isotropic) are always "active".  Lets the optimizer
    // discount a channel's variance on its fallback events and, via
    // NestedMixtureChannel, drive a whole group of fallback directors down with
    // a single outer weight.
    virtual bool DirectingActive(
        siren::dataclasses::InteractionRecord const &) const { return true; }

    // Evaluate density and optimizer activity together. Directed channels use
    // the shared regime memoization, so callers that need both do not repeat
    // the same geometry classification.
    virtual PhaseSpaceChannelEvaluation Evaluate(
        std::shared_ptr<siren::detector::DetectorModel const> detector_model,
        siren::dataclasses::InteractionRecord const & record) const
    {
        return {Density(detector_model, record), DirectingActive(record)};
    }
};

